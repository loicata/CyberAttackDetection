def _sha256_file(file_path: Path) -> str:
    """Compute SHA-256 hash of a file.

    Args:
        file_path: Path to the file.

    Returns:
        Hex digest string.
    """
    import hashlib

    sha256 = hashlib.sha256()
    with open(file_path, "rb") as fh:
        for chunk in iter(lambda: fh.read(8192), b""):
            sha256.update(chunk)
    return sha256.hexdigest()


def _build_report_data(
    alert: Alert,
    timeline: list[dict[str, Any]] | None,
    evidence_files: list[dict[str, Any]] | None,
    intel_data: dict[str, Any] | None,
    response_actions: list[dict[str, Any]] | None,
) -> dict[str, Any]:
    """Build the report data dictionary.

    Args:
        alert: The alert.
        timeline: Timeline entries.
        evidence_files: Evidence records.
        intel_data: Intel data.
        response_actions: Response actions.

    Returns:
        Complete report data dictionary.
    """
    # Extract with fallback to raw Suricata JSON
    raw_data = {}
    if alert.raw_event and hasattr(alert.raw_event, "data"):
        raw_data = alert.raw_event.data or {}
    raw_entry = raw_data.get("raw_entry", {})

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "alert_uid": alert.alert_uid,
        "title": alert.title,
        "severity": alert.severity.value,
        "score": alert.score,
        "alert_type": alert.alert_type.value,
        "status": alert.status.value,
        "created_at": alert.created_at,
        "description": alert.description,
        "source_ip": alert.source_ip or raw_entry.get("src_ip"),
        "source_port": alert.source_port or raw_entry.get("src_port"),
        "dest_ip": alert.dest_ip or raw_entry.get("dest_ip"),
        "dest_port": alert.dest_port or raw_entry.get("dest_port"),
        "process_name": alert.process_name or raw_data.get("process_name"),
        "process_pid": alert.process_pid or raw_data.get("process_pid"),
        "file_path": alert.file_path or raw_data.get("file_path"),
        "intel_data": intel_data,
        "timeline": timeline or [],
        "evidence_files": evidence_files or [],
        "response_actions": response_actions or [],
    }


def _render_html(data: dict[str, Any]) -> str:
    """Render the HTML report from data.

    Uses Jinja2 if available, falls back to simple string replacement.

    Args:
        data: Report data dictionary.

    Returns:
        HTML string.
    """
    try:
        from jinja2 import Template

        template = Template(HTML_TEMPLATE)
        return template.render(**data)
    except ImportError:
        logger.warning("Jinja2 not available, using basic HTML rendering")
        return _basic_html_render(data)


def _basic_html_render(data: dict[str, Any]) -> str:
    """Minimal HTML rendering without Jinja2.

    Args:
        data: Report data dictionary.

    Returns:
        Basic HTML string.
    """
    html = f"""<!DOCTYPE html>
<html><head><title>Incident Report - {data['alert_uid']}</title></head>
<body>
<h1>Incident Report</h1>
<p>Alert: {data['title']}</p>
<p>Severity: {data['severity']} | Score: {data['score']}/100</p>
<p>Description: {data['description']}</p>
<p>Generated: {data['generated_at']}</p>
<pre>{json.dumps(data, indent=2, default=str)}</pre>
</body></html>"""
    return html
