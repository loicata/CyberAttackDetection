def generate_report(
    alert: Alert,
    config: AppConfig,
    timeline: list[dict[str, Any]] | None = None,
    evidence_files: list[dict[str, Any]] | None = None,
    intel_data: dict[str, Any] | None = None,
    response_actions: list[dict[str, Any]] | None = None,
) -> tuple[Path, Path]:
    """Generate forensic report in HTML and JSON formats.

    Args:
        alert: The alert to report on.
        config: Application configuration.
        timeline: Optional event timeline entries.
        evidence_files: Optional list of evidence file records.
        intel_data: Optional threat intelligence data.
        response_actions: Optional list of response actions taken.

    Returns:
        Tuple of (html_path, json_path).

    Raises:
        ForensicError: If report generation fails.
    """
    report_dir = Path(config.forensics.report_dir)
    report_dir.mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    base_name = f"report_{alert.alert_uid[:8]}_{timestamp}"

    report_data = _build_report_data(
        alert, timeline, evidence_files, intel_data, response_actions
    )

    json_path = report_dir / f"{base_name}.json"
    html_path = report_dir / f"{base_name}.html"

    try:
        json_content = json.dumps(report_data, indent=2, default=str)
        json_path.write_text(json_content, encoding="utf-8")
    except OSError as exc:
        raise ForensicError(f"Failed to write JSON report: {exc}") from exc

    try:
        html_content = _render_html(report_data)
        html_path.write_text(html_content, encoding="utf-8")
    except Exception as exc:
        raise ForensicError(f"Failed to write HTML report: {exc}") from exc

    # Generate forensic ZIP archive for authorities
    zip_path = _create_forensic_archive(
        report_dir, base_name, html_path, json_path,
        alert, config, evidence_files,
    )
    if zip_path:
        logger.info("Forensic archive: %s", zip_path)

    logger.info("Reports generated: %s, %s", html_path, json_path)
    return html_path, json_path


def _create_forensic_archive(
    report_dir: Path,
    base_name: str,
    html_path: Path,
    json_path: Path,
    alert: Alert,
    config: AppConfig,
    evidence_files: list[dict[str, Any]] | None,
) -> Path | None:
    """Create a ZIP archive containing the full forensic package.

    The archive is structured for submission to law enforcement or
    incident response teams. It contains:
    - README.txt with chain-of-custody metadata
    - The HTML report (human-readable)
    - The JSON report (machine-readable)
    - All evidence files (snapshots, process lists, network, registry, timeline)
    - SHA-256 checksums of all included files (integrity verification)

    Args:
        report_dir: Output directory for the archive.
        base_name: Base filename (without extension).
        html_path: Path to the generated HTML report.
        json_path: Path to the generated JSON report.
        alert: The alert being reported.
        config: Application configuration.
        evidence_files: Evidence file records from the database.

    Returns:
        Path to the ZIP archive, or None if creation failed.
    """
    import hashlib
    import zipfile

    zip_path = report_dir / f"{base_name}.zip"
    evidence_dir = Path(config.forensics.evidence_dir) / alert.alert_uid

    try:
        checksums: list[tuple[str, str]] = []

        with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
            # ---- README with chain-of-custody info ----
            readme = _generate_readme(alert, config, evidence_files)
            zf.writestr(f"{base_name}/README.txt", readme)

            # ---- JSON Report (machine-readable, for SIEM/SOAR tools) ----
            if json_path.exists():
                zf.write(json_path, f"{base_name}/report.json")
                checksums.append(("report.json", _sha256_file(json_path)))

            # ---- Evidence files ----
            if evidence_dir.exists():
                for evidence_file in sorted(evidence_dir.iterdir()):
                    if evidence_file.is_file():
                        arc_name = f"{base_name}/evidence/{evidence_file.name}"
                        zf.write(evidence_file, arc_name)
                        checksums.append(
                            (f"evidence/{evidence_file.name}", _sha256_file(evidence_file))
                        )

            # ---- SHA-256 checksum manifest ----
            checksum_text = "SHA-256 Integrity Checksums\n"
            checksum_text += "=" * 60 + "\n"
            checksum_text += f"Generated: {datetime.now(timezone.utc).isoformat()}\n"
            checksum_text += f"Alert UID: {alert.alert_uid}\n"
            checksum_text += "=" * 60 + "\n\n"
            for filename, sha in checksums:
                checksum_text += f"{sha}  {filename}\n"
            zf.writestr(f"{base_name}/SHA256SUMS.txt", checksum_text)

        logger.info(
            "Forensic archive created: %s (%d files, %.1f KB)",
            zip_path, len(checksums) + 2, zip_path.stat().st_size / 1024,
        )
        return zip_path

    except Exception as exc:
        logger.error("Failed to create forensic archive: %s", exc)
        return None


